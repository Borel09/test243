function drawChessboard(num) {
  let gridSize = num;
  let chessBoard = ''
  for(let i = 0; i < gridSize; i++){
    for(let j = 0; j < gridSize; j++){
      if((i + j) % 2 === 0){
        chessBoard += ' ';
      }else {
        chessBoard += '#';
      }
    }
    chessBoard += "\n";
  }
  console.log(chessBoard);
}
